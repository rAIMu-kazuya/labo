public class TempAA {

	private String item = "TempAB";
	private int TempFB;
	/**
	 * コンストラクタです．引数(TempFB:int)はTempEBです．
	 * 
	 * TempEB(引数TempFB)をインスタンス変数TempFBに代入します．
	 * 
	 * @param ??
	 */
	public TempAA(int TempFB) {
    this.TempFB = TempFB;
	}
	/**
	 * 品名を返却します．
	 * 
	 * インスタンス変数itemを返却します．
	 * 
	 * @return
	 */
	public String getItem() {
		return item;
	}
	/**
	 * TempEBを返却します．
	 * 
	 * インスタンス変数TempFBを返却します．
	 * 
	 * @return
	 */
	public int TempGB() {
		return this.TempFB;
	}
}